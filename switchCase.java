import java.util.*;
public class switchCase{

     public static void main(String[] args) {

        String st="cks";
        switch(st){
            case "abc":
            System.out.print("abc");
            break;
            case "cks":
            System.out.print("cks");
            break;
            default:
            System.out.print("not found");
        }
    }
}